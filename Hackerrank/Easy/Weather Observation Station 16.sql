

/*
    SORTING SOLUTION
*/
SELECT LAT_N FROM STATION
WHERE 
    LAT_N > 38.7780
ORDER BY LAT_N
LIMIT 1;



/* DIRECT SOLUTION */
SELECT FORMAT( MIN( LAT_N ), 4 ) FROM STATION
WHERE 
    LAT_N > 38.7780;



/*
    SUBQUERY SOLUTION
*/
SELECT FORMAT( LAT_N, 4) FROM STATION
WHERE LAT_N == (
    SELECT MIN(LAT_N) FROM STATION
    WHERE
        LAT_N > 38.7780
)

